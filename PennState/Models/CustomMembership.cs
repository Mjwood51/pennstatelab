        public override bool ValidateUser(string username, string password)
        {
            if (string.IsNullOrEmpty(username) || string.IsNullOrEmpty(password))
            {
                return false;
            }
            using (ContextModel _context = new ContextModel())
            {
                var hashedpword = _context.Tbl_Users.Where(x => x.UserName.ToLower() == username.ToLower()).FirstOrDefault();

                if (hashedpword != null)
                {
                    byte[] hashBytes = Convert.FromBase64String(hashedpword.PasswordHashed);

                    byte[] salt = new byte[16];
                    Array.Copy(hashBytes, 0, salt, 0, 16);
                    /* Compute the hash on the password the user entered */
                    var pbkdf2 = new Rfc2898DeriveBytes(password, salt, 10000);
                    byte[] hash = pbkdf2.GetBytes(20);
                    /* Compare the results */
                    for (int i = 0; i < 20; i++)
                    {
                        if (hashBytes[i + 16] != hash[i])
                        {
                            return false;
                            throw new UnauthorizedAccessException();
                        }
                    }
                    return true;
                }
                else
                {
                    return false;
                }
            }
        }

